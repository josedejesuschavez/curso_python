# Definición de variables
entero = 42               # Variable de tipo entero
flotante = 3.14           # Variable de tipo flotante
cadena = "Hola, mundo!"   # Variable de tipo cadena
booleano = True           # Variable de tipo booleano

# Imprimir valores de las variables
print("Entero:", entero, type(entero))
print("Flotante:", flotante, type(flotante))
print("Cadena:", cadena, type(cadena))
print("Booleano:", booleano, type(booleano))

# Operaciones con variables
suma = entero + 10                          # Suma de un entero y un entero
resta = flotante - 1.5                      # Resta de un flotante y un flotante
concatenacion = cadena + " Soy un string."  # Concatenación de cadenas
comparacion = entero > 50                   # Comparación de un entero con un valor
negacion = not booleano                     # Negación de un booleano

# Imprimir resultados de operaciones
print("Suma:", suma)
print("Resta:", resta)
print("Concatenación:", concatenacion)
print("Comparación:", comparacion)
print("Negación:", negacion)
