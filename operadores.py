# Operadores aritméticos, de comparación y lógicos en Python

# Operadores aritméticos
a = 10
b = 5

suma = a + b            # Suma
resta = a - b           # Resta
multiplicacion = a * b  # Multiplicación
division = a / b        # División
modulo = a % b          # Módulo (resto de la división)
potencia = a ** b       # Potencia

# Imprimir resultados de operadores aritméticos
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Módulo:", modulo)
print("Potencia:", potencia)

# Operadores de comparación
x = 10
y = 20

igual = x == y          # Igualdad
diferente = x != y      # Diferencia
mayor_que = x > y       # Mayor que
menor_que = x < y       # Menor que
mayor_igual = x >= y    # Mayor o igual que
menor_igual = x <= y    # Menor o igual que

# Imprimir resultados de operadores de comparación
print("Igualdad:", igual)
print("Diferencia:", diferente)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_igual)
print("Menor o igual que:", menor_igual)

# Operadores lógicos
p = True
q = False

and_logico = p and q   # AND lógico
or_logico = p or q     # OR lógico
not_logico = not p     # NOT lógico

# Imprimir resultados de operadores lógicos
print("AND lógico:", and_logico)
print("OR lógico:", or_logico)
print("NOT lógico:", not_logico)
